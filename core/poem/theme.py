_META_PREFIXES = ["意向", "意象", "意境", "主题", "元素", "风格", "色调", "感觉", "氛围"]


def resolve_theme_synonyms(theme: str) -> list:
    """将用户输入的主题词归一化后查找同义词列表。"""
    if theme in THEME_SYNONYMS:
        return THEME_SYNONYMS[theme]
    stripped = re.sub(r"[天日夜季时节晨昏景]$", "", theme).strip()
    if stripped and stripped in THEME_SYNONYMS:
        return THEME_SYNONYMS[stripped]
    for key in THEME_SYNONYMS:
        if key in theme:
            return THEME_SYNONYMS[key]
    return [theme]


def strip_meta_prefix(item: str) -> str:
    """剥离元词汇前缀，'意向荷花'→'荷花'。"""
    for prefix in _META_PREFIXES:
        if item.startswith(prefix) and len(item) > len(prefix):
            return item[len(prefix):]
    return item


def get_imagery_synonyms(item: str) -> list:
    """获取必须意象词的同义词列表，使'荷花'可匹配'荷叶'/'红蕖'等。"""
    synonyms = {item}
    if item in THEME_SYNONYMS:
        synonyms.update(THEME_SYNONYMS[item])
        return list(synonyms)
    prefix_hits = [(k, v) for k, v in THEME_SYNONYMS.items() if item.startswith(k)]
    if prefix_hits:
        key, syns = max(prefix_hits, key=lambda x: len(x[0]))
        synonyms.update(syns)
        synonyms.add(key)
        return list(synonyms)
    for key, syns in THEME_SYNONYMS.items():
        if item in syns:
            synonyms.update(syns)
            synonyms.add(key)
            return list(synonyms)
    stripped = re.sub(r"[花叶枝香色水天日]$", "", item).strip()
    if stripped and stripped != item and stripped in THEME_SYNONYMS:
        synonyms.update(THEME_SYNONYMS[stripped])
        return list(synonyms)
    return list(synonyms)


